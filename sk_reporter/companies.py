"""Единый список компаний-подрядчиков для docx и AI-агентов."""

from dataclasses import dataclass


@dataclass(frozen=True, slots=True)
class Company:
    """Одна компания: имя в отчётах, ключевые слова, опционально другое имя болванки."""

    name: str
    keywords: list[str]
    template: str | None = None

    @property
    def template_stem(self) -> str:
        """Имя файла болванки без расширения (data/templates/{template_stem}.docx)."""
        return self.template if self.template is not None else self.name


