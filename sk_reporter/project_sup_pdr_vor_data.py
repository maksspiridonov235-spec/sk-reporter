"""ВОР пилота SUP-PDR — эталон в репо (слово в слово из Word), без парсера."""

from __future__ import annotations

from typing import Any


def _section(name: str) -> dict[str, str]:
    return {"kind": "section", "name": name}


def _work(name: str, unit: str, quantity: str, note: str = "", num: str = "") -> dict[str, str]:
    return {
        "kind": "work",
        "num": num,
        "name": name,
        "unit": unit,
        "quantity": quantity,
        "note": note,
    }


