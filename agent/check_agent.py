"""
Агент проверки отчётов строительного контроля.
Проверяет:
1. Содержимое описания действий (что реально проинспектировал инженер)
2. Объемы работ (нет ли превышения)
3. Нулевые объемы (работы с суточным объемом = 0)
Выводит вариант правильного заполнения.
"""

import re
from pathlib import Path
from docx import Document

MODEL = "gemma4:31b-cloud"

SYSTEM_PROMPT = """Ты — ведущий инженер строительного контроля. Проверяешь качество отчётов.

КРИТЕРИИ ПРОВЕРКИ:
1. ОПИСАНИЕ: Для каждого вида работ — есть ли описание контроля (что проверялось, каким методом, результат). Если вид работ тебе незнаком — не пиши ничего про него.
2. ОБЪЕМЫ: Накопительный объем ≤ Проектный объем
3. НУЛЕВЫЕ ОБЪЕМЫ: Работы с суточным объемом = 0 нужно УДАЛИТЬ

ВАЖНО:
- Не упоминай ГОСТы и нормативы — не требуй их наличия
- Если не уверен в виде работ — лучше пропусти, не выдумывай
- Пиши только о реально найденных проблемах

ФОРМАТ ОТВЕТА:

## РЕЗЮМЕ ПРОВЕРКИ
- [✓ или ⚠] Объёмы: [в порядке / перечисли превышения]
- [✓ или ⚠] Нулевые объёмы: [нет / перечисли работы для удаления]
- [✓ или ⚠] Описания: [все верны / исправлено N — перечисли названия работ]

## ИСПРАВЛЕННЫЙ ОТЧЁТ
Воспроизведи ПОЛНОСТЬЮ весь раздел отчёта в двух частях:
ЧАСТЬ 1 — «Инспекционный контроль по проведённым работам:» + все пункты с объёмами. Названия работ переформатируй: убери «Инспекционный контроль» из начала, устрани тавтологии и дублирование, исправь пунктуацию — сделай название лаконичным и профессиональным. Объёмы копируй дословно.
ЧАСТЬ 2 — «Наряд-допуск проверен...» + исправленные описания по каждому пункту"""


def extract_full_text(filepath: str) -> str:
    """
    Извлекает весь текст из DOCX файла.
    """
    try:
        doc = Document(filepath)
        parts = []

        # Вытаскиваем все параграфы
        for para in doc.paragraphs:
            text = para.text.strip()
            if text:
                parts.append(text)

        # Вытаскиваем все таблицы
        for table in doc.tables:
            for row in table.rows:
                row_text = " | ".join(cell.text.strip() for cell in row.cells)
                if row_text.strip():
                    parts.append(row_text)

        return "\n".join(parts)
    except Exception as e:
        print(f"[CHECK_AGENT] extract_full_text error: {e}")
        return ""


def check_report(filepath: str) -> dict:
    """
    Проверяет отчет по всем критериям.
    """
    filename = Path(filepath).name

    # Извлекаем весь текст
    full_text = extract_full_text(filepath)

    if not full_text:
        return {
            "ok": False,
            "report": "Не удалось прочитать содержимое отчета",
            "_source_file": filename,
        }

    user_prompt = f"""Проверь этот отчет строительного контроля.

Структура отчёта:
- ПЕРВАЯ ЧАСТЬ: список видов работ с объемами (пункты 1, 2, 3...), начинается с «Инспекционный контроль по видам работ:»
- ВТОРАЯ ЧАСТЬ: описание действий, начинается с «Наряд-допуск проверен» или «Работы ведутся в соответствии»

---ТЕКСТ ОТЧЕТА---
{full_text}
---КОНЕЦ ТЕКСТА---

Для каждого вида работ из первой части внутренне проверь:
1. Накопительный объем ≤ Проектный объем (если превышение — отметь в резюме)
2. Объем за сутки ≠ 0 (если = 0 — отметь в резюме как работу для удаления)
3. Есть ли во второй части описание контроля (ЧТО проверялось, КАКИМ методом, РЕЗУЛЬТАТ). Если описания нет или оно формальное — исправь во второй части ИСПРАВЛЕННОГО ОТЧЁТА.

Выведи ответ строго в формате:

## РЕЗЮМЕ ПРОВЕРКИ
- [✓ или ⚠] Объёмы: [в порядке / перечисли превышения]
- [✓ или ⚠] Нулевые объёмы: [нет / перечисли работы для удаления]
- [✓ или ⚠] Описания: [все верны / исправлено N — перечисли названия работ]

## ИСПРАВЛЕННЫЙ ОТЧЁТ
ЧАСТЬ 1 (переформатируй названия работ и нормализуй строки объёмов):
Инспекционный контроль по проведённым работам:
1. [название работы 1 — без слов «Инспекционный контроль» в начале, только суть работы]
Проектный объем: ...
Объем за сутки: ...
Накопительный объем: ...
(и так для каждой работы — ни одну не пропускай)

Правило для названий работ:
- Убери слова «Инспекционный контроль» из начала названия
- Исправь пунктуацию: убери лишние запятые, незакрытые скобки, лишние пробелы
- Переформулируй название профессионально и лаконично — убери тавтологии и дублирование. Например: «Сборка и сварка (укрупнённая сборка) балок Б2» → «Укрупнённая сборка и сварка балок Б2»

Правило для строк объёмов (Проектный объем / Объем за сутки / Накопительный объем):
- Значение и единицы измерения копируй точно
- Формат: «Проектный объем – 10 шт» (пробел до тире, пробел после тире, пробел перед единицей)
- Убери пустые скобки «()» и «( )» если есть — это лишнее

ЧАСТЬ 2 (описание действий — для проблемных пиши исправленное, для корректных копируй оригинал):
Наряд-допуск проверен, замечаний нет. Работы ведутся в соответствии с технологическими картами и РД. Применяемый материал прошел процедуру входного контроля.
1. [описание контроля для работы 1]
2. [описание контроля для работы 2]
(и так для каждой работы — ни одну не пропускай)

ВАЖНО: Выводи ТОЛЬКО эти два раздела. Никаких других разделов, пояснений или комментариев."""

    # Вызываем ollama
    try:
        import ollama
        response = ollama.chat(
            model=MODEL,
            messages=[
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": user_prompt}
            ],
            stream=False,
        )

        report_text = response.get("message", {}).get("content", "").strip()

        if not report_text:
            return {
                "ok": False,
                "report": "Ошибка: пустой ответ модели",
                "_source_file": filename,
            }

        # Проверяем есть ли проблемы
        has_issues = "в порядке" not in report_text.lower() or "нет" not in report_text.lower()

        result = {
            "ok": not has_issues,
            "report": report_text,
            "_source_file": filename,
        }

        print(f"[CHECK_AGENT] {'OK' if result['ok'] else 'ISSUES'}: {filename}")
        return result

    except Exception as e:
        print(f"[CHECK_AGENT] Error calling ollama: {e}")
        return {
            "ok": False,
            "report": f"Ошибка проверки: {str(e)}",
            "_source_file": filename,
        }
