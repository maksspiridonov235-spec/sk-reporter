"""
Агент проверки отчётов строительного контроля.
Проверяет:
1. Содержимое описания действий (что реально проинспектировал инженер)
2. Объемы работ (нет ли превышения)
3. Нулевые объемы (работы с суточным объемом = 0)
Выводит вариант правильного заполнения.
"""

import re
from pathlib import Path
from docx import Document

MODEL = "gemma4:31b-cloud"

SYSTEM_PROMPT = """Ты — ведущий инженер строительного контроля. Проверяешь качество отчётов.

КРИТЕРИИ ПРОВЕРКИ:
1. ОПИСАНИЕ: Для каждого вида работ — есть ли описание контроля (что проверялось, каким методом, результат). Если вид работ тебе незнаком — не пиши ничего про него.
2. ОБЪЕМЫ: Накопительный объем ≤ Проектный объем
3. НУЛЕВЫЕ ОБЪЕМЫ: Работы с суточным объемом = 0 нужно УДАЛИТЬ

ВАЖНО:
- Не упоминай ГОСТы и нормативы — не требуй их наличия
- Если не уверен в виде работ — лучше пропусти, не выдумывай
- Пиши только о реально найденных проблемах

ФОРМАТ ОТВЕТА:

## 1. ПРОВЕРКА ОПИСАНИЯ ДЕЙСТВИЙ
**РАБОТА:** [название]
**ПРОБЛЕМА:** [в чем недостаток]
**НУЖНО ДОБАВИТЬ:** [что должно быть написано]

## 2. ПРОВЕРКА ОБЪЕМОВ
[проверка превышений или ОК]

## 3. ПРОВЕРКА НУЛЕВЫХ ОБЪЕМОВ
[работы с суточным объемом = 0 или ОК]

## 4. ВАРИАНТ ЗАПОЛНЕНИЯ
Для каждой проблемы покажи правильный вариант описания.

## 5. ИСПРАВЛЕННЫЙ ОТЧЁТ (ПОЛНЫЙ ТЕКСТ)
Воспроизведи ПОЛНОСТЬЮ весь раздел отчёта в двух частях:
ЧАСТЬ 1 — «Инспекционный контроль по видам работ:» + все пункты с объёмами (точно как в оригинале)
ЧАСТЬ 2 — «Наряд-допуск проверен...» + исправленные описания по каждому пункту"""


def extract_full_text(filepath: str) -> str:
    """
    Извлекает весь текст из DOCX файла.
    """
    try:
        doc = Document(filepath)
        parts = []

        # Вытаскиваем все параграфы
        for para in doc.paragraphs:
            text = para.text.strip()
            if text:
                parts.append(text)

        # Вытаскиваем все таблицы
        for table in doc.tables:
            for row in table.rows:
                row_text = " | ".join(cell.text.strip() for cell in row.cells)
                if row_text.strip():
                    parts.append(row_text)

        return "\n".join(parts)
    except Exception as e:
        print(f"[CHECK_AGENT] extract_full_text error: {e}")
        return ""


def check_report(filepath: str) -> dict:
    """
    Проверяет отчет по всем критериям.
    """
    filename = Path(filepath).name

    # Извлекаем весь текст
    full_text = extract_full_text(filepath)

    if not full_text:
        return {
            "ok": False,
            "report": "Не удалось прочитать содержимое отчета",
            "_source_file": filename,
        }

    # Создаем промпт для LLM
    user_prompt = f"""Проверь этот отчет строительного контроля по ВСЕМ критериям:

---ТЕКСТ ОТЧЕТА---
{full_text}
---КОНЕЦ ТЕКСТА---

## 1. ПРОВЕРКА ОПИСАНИЯ ДЕЙСТВИЙ
Отчёт строительного контроля имеет структуру:
- ПЕРВАЯ ЧАСТЬ: список видов работ с объемами (пункты 1, 2, 3...)
- ВТОРАЯ ЧАСТЬ: описание действий — всегда начинается с фраз "Наряд-допуск проверен" или "Работы ведутся в соответствии" — содержит описание контроля по каждому пункту

Для каждого вида работ из первой части:
- Найди соответствующее описание во второй части (по номеру или названию работы)
- Определи сам, какой контроль должен быть выполнен для этого вида работ
- Проверь, есть ли в описании: ЧТО проверялось, КАКИМ методом, РЕЗУЛЬТАТ (принято/не принято)
- Если для работы нет описания во второй части — это проблема
- Если вид работ тебе незнаком — пропусти, ничего не пиши про него
- Пиши только о реально найденных проблемах, не выдумывай

Для каждого недостатка напиши:
**РАБОТА:** [название]
**ПРОБЛЕМА:** [в чем недостаток]
**НУЖНО ДОБАВИТЬ:** [что должно быть написано]

## 2. ПРОВЕРКА ОБЪЕМОВ
Проверь все работы где указаны объемы:
- Накопительный объем ≤ Проектный объем
- Если превышение - укажи какое

Если ошибок нет напиши: Объемы в порядке.

## 3. ПРОВЕРКА НУЛЕВЫХ ОБЪЕМОВ
Ищи работы где "Объем за сутки" = 0:
- Если есть - напиши какие нужно УДАЛИТЬ
- Если нет - напиши: Нулевых объемов нет.

## 4. ВАРИАНТ ПРАВИЛЬНОГО ЗАПОЛНЕНИЯ
Для каждой найденной проблемы в описании покажи пример правильного заполнения:

**БЫЛО:**
[ошибка]

**ИСПРАВИТЬ НА:**
[правильный вариант, конкретный, с примерами]

## 5. ИСПРАВЛЕННЫЙ ОТЧЁТ (ПОЛНЫЙ ТЕКСТ)
ОБЯЗАТЕЛЬНО воспроизведи ВСЕ ДВЕ ЧАСТИ отчёта полностью — именно в таком порядке:

ЧАСТЬ 1 (скопируй из оригинала ДОСЛОВНО, ничего не меняй):
Инспекционный контроль по видам работ:
1. [название работы 1]
Проектный объем: ...
Объем за сутки: ...
Накопительный объем: ...
2. [название работы 2]
Проектный объем: ...
Объем за сутки: ...
Накопительный объем: ...
(и так для каждой работы)

ЧАСТЬ 2 (описание действий — исправь проблемные, остальные скопируй из оригинала):
Наряд-допуск проверен, замечаний нет. Работы ведутся в соответствии с технологическими картами и РД. Применяемый материал прошел процедуру входного контроля.
1. [описание контроля для работы 1]
2. [описание контроля для работы 2]
(и так для каждой работы)

ВАЖНО:
- Часть 1 — копируй из оригинала ТОЧНО, без каких-либо изменений
- Часть 2 — для работ с правильным описанием копируй оригинал, для проблемных пиши исправленное описание
- Не пропускай ни одну работу ни в первой, ни во второй части"""

    # Вызываем ollama
    try:
        import ollama
        response = ollama.chat(
            model=MODEL,
            messages=[
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": user_prompt}
            ],
            stream=False,
        )

        report_text = response.get("message", {}).get("content", "").strip()

        if not report_text:
            return {
                "ok": False,
                "report": "Ошибка: пустой ответ модели",
                "_source_file": filename,
            }

        # Проверяем есть ли проблемы
        has_issues = "в порядке" not in report_text.lower() or "нет" not in report_text.lower()

        result = {
            "ok": not has_issues,
            "report": report_text,
            "_source_file": filename,
        }

        print(f"[CHECK_AGENT] {'OK' if result['ok'] else 'ISSUES'}: {filename}")
        return result

    except Exception as e:
        print(f"[CHECK_AGENT] Error calling ollama: {e}")
        return {
            "ok": False,
            "report": f"Ошибка проверки: {str(e)}",
            "_source_file": filename,
        }
